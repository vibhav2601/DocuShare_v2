package com.example.docusharev2;

public class models {
    private String websiteName1;
    private String websiteName2;
    private String websiteName3;
    private String websiteName4;

    public models() {
    }

    public models(String websiteName1, String websiteName2, String websiteName3, String websiteName4) {
        this.websiteName1 = websiteName1;
        this.websiteName2 = websiteName2;
        this.websiteName3 = websiteName3;
        this.websiteName4 = websiteName4;
    }

    public String getWebsiteName1() {
        return websiteName1;
    }

    public void setWebsiteName1(String websiteName1) {
        this.websiteName1 = websiteName1;
    }

    public String getWebsiteName2() {
        return websiteName2;
    }

    public void setWebsiteName2(String websiteName2) {
        this.websiteName2 = websiteName2;
    }

    public String getWebsiteName3() {
        return websiteName3;
    }

    public void setWebsiteName3(String websiteName3) {
        this.websiteName3 = websiteName3;
    }

    public String getWebsiteName4() {
        return websiteName4;
    }

    public void setWebsiteName4(String websiteName4) {
        this.websiteName4 = websiteName4;
    }
}
